const loadImageDimensions = (src) => {
  return new Promise((resolve) => {
    const img = new Image();
    img.src = src;
    img.onload = () => {
      resolve({
        src,
        width: img.width,
        height: img.height,
      });
    };
  });
};

export const getGalleryImages = async () => {
  const imageData = await Promise.all(galleryImages.map(loadImageDimensions));
  return imageData;
};

